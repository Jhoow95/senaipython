nota = float(input('digite uma nota: '))
             
if nota > 10:
        print('você digitou número inválido')
else:
        print('você não digitou maior que 10')
if nota >= 5:
        print('Aprovado')
else:
        print('você não digitou maior/igual que 5')
if nota >= 3:
        print('Recuperação')

if nota < 0:
        print('você digitou número inválido')
else:
        print('você não digitou menor que 0 - e está Reprovada')